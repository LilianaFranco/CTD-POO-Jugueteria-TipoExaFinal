public class JugueteGenerico extends Juguete{

    //Atributos
    private int aptoParaEdad;

    //Constructor
    public JugueteGenerico(String nombre, Double precio, int aptoParaEdad) {
        super(nombre, precio);
        this.aptoParaEdad = aptoParaEdad;
    }

    @Override
    public Double getPrecio() {
        return getPrecio();
    }
}
